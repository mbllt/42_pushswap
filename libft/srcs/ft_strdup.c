#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tab;
	int		i;

	tab = NULL;
	tab = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!tab)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
